public class MethodVisitor extends VoidVisitorAdapter<Map<String,MethodDeclaration>> {

	@Override
	public void visit(MethodDeclaration n,  Map<String,MethodDeclaration> arg) {
		if (Character.isUpperCase(n.getName().charAt(0))) {
			arg.put(n.getName(), n);
		}
		super.visit(n, arg);
	}
}	